public class SquareCube {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            int square = i * i;
            System.out.println("Square of " + i + " is " + square);
        }

        for (int i = 1; i <= 10; i++) {
            int cube = i * i * i;
            System.out.println("Cube of " + i + " is " + cube);
        }


    }
}




